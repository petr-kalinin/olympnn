<p>В течение тура участникам будет предоставлен небольшой перекус, также во все время тура участники будут обеспечены питьевой водой. Участникам рекомендуется иметь с собой ручку и бумагу для черновиков; при желании участники могут также взять с собой на тур еду. Использование во время тура каких-либо электронных устройств, носителей информации, литературы и записей, сделанных до тура, не разрешено.</p>

<h2>Клавиатуры и мыши</h2>

<p>Участники могут использовать на туре свои клавиатуры и мышки при выполнении следующих требований:</p>

<ul>
<li>Устройства (клавиатуры и мышки) должны подключаться по USB и не требовать специальных драйверов.</li>
<li>У устройств должны отсутствовать дополнительные функции, и вообще устройства должны быть более-менее обычными. Степень обычности определяется жюри.</li>
<li>В частности, на клавиатурах должны отсутствовать дополнительные экраны, какие-либо совсем нестандартные кнопки (более-менее обычные медиакнопки допускаются), и т.д.</li>
<li>Устройства не должны создавать помех другим участникам олимпиады (в частности, не допускаются клавиатуры с особенно громкими клавишами).</li>
<li>Устройства должны быть предъявлены на регистрации; жюри имеет право по своему усмотрению запретить использование любого устройства.</li>
</ul>

<p>Компьютеры участников, участвующих за полноценными компьютерами, будут оборудованы стандартными клавиатурой и мышью, поэтому мы считаем, что этим участникам большой необходимости приносить свои клавиатуры и мыши нет. </p>

<p>Участникам, участвующим за ноутбуками, будут предоставлены стандартные мыши и, возможно, клавиатуры. </p>

