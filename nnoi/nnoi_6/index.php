<h1>VI Нижегородская городская олимпиада школьников по информатике</h1>
<p>
VI Нижегородская городская олимпиада школьников по информатике была проведена 4 февраля 2010&nbsp;г. 
на четырех площадках одновременно: <a href="http://ipfran.ru/training/training_center.html">НОЦ ИПФ
РАН</a>, <a href="http://www.unn.ru/">ННГУ</a>, <a href="http://lic40.nnov.ru/">лицей 40</a> и школа
30. В олимпиаде приняли участие 70 участников (в том числе 6 школьников из г. Саров и 2 &#8212 из
Балахны), 22 из которых были награждены дипломами.
</p>
