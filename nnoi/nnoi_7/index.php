<h1>VII Нижегородская городская олимпиада школьников по информатике</h1>
<p>
VII Нижегородская городская олимпиада школьников по информатике была проведена 3 февраля 2011&nbsp;г. 
на трех площадках одновременно: <a href="http://ipfran.ru/training/training_center.html">НОЦ ИПФ
РАН</a>, <a href="http://www.unn.ru/">ННГУ</a> и <a href="http://lic40.nnov.ru/">лицей 40</a>. 
В олимпиаде приняли участие 59 участников, 16 из которых были награждены дипломами.
