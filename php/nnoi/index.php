<h1>Нижегородские городские олимпиады школьников по информатике</h1>
<p>
19 февраля 2005 года в Нижнем Новгороде, впервые после 4-летнего перерыва, была проведена 
городская олимпиада школьников по информатике; с тех пор олимпиада проводится ежегодно. 
Олимпиада проводится среди учащихся 6-11 классов образовательных учреждений города
Нижнего Новгорода с целью поиска талантливой молодёжи и привлечения её в науку,
активизации работы факультативов, спецкурсов, кружков, повышения уровня преподавания
предметов естественнонаучного цикла. В олимпиаде ежегодно участвуют около 80 участников, из 
которых около 30 награждаются дипломами.
</p><p>
Организаторами олимпиады являются департамент
образования и социально-правовой защиты детства администрации города Нижнего Новгорода
и Нижегородский государственный университет им. Н.И. Лобачевского. 
Спонсорами олимпиады выступают ведущие IT-компании Нижнего Новгорода: компания 
<a href="http://www.intel.com/corporate/europe/emea/rus/country/jobs/sites/nnovgorod.htm">Intel-Нижний 
Новгород</a> и <a href="http://www.meranetworks.ru/">компания Mera</a>.
</p><p>
Это пока единственная в Нижнем Новгороде олимпиада, где проверка решений 
осуществляется открытой автоматической системой тестирования сразу после окончания олимпиады 
и демонстрируется на экране всем участникам и приглашённым. 
</p><p>
Олимпиада проводится в соответствии с Положением о городской олимпиаде по информатике (приказы 
департамента образования и социально-правовой защиты детства администрации г. Нижнего Новгорода 
№416 от 16.11.04, №1453 от 07.11.2008).
</p>
