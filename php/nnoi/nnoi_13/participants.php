<li>Толмачев Александр, 10 класс, школа 3 г. Саров.</li>
</ul>

<p>Олимпиада состоится в субботу 18 февраля во 2 корпусе ННГУ (пр. Гагарина, 23). Регистрация с 9.30 до 10.00.</p>

