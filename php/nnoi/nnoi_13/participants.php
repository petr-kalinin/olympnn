<h1>Список участников XIII Нижегородской городской олимпиады</h1>
<p>Жюри Нижегородской городской олимпиады школьников по информатике им. В.Д.Лелюха рассмотрело поступившие заявки на участие в олимпиаде и решило пригласить на олимпиаду напрямую:</p>
<ul>
<li>всех призеров прошлогодней областной олимпиады,</li>
<li>всех 9-классников и младше, набравших на районной олимпиаде 250 и более баллов, </li>
<li>всех 10-классников, набравших на районной олимпиаде 180 и более баллов,</li>
<li>всех 11-классников, набравших на районной олимпиаде 200 и более баллов.</li>
</ul>

<p>Кроме того, к участию в олимпиаде приглашаются лучшие школьники по <a href="zaoch/results.php">результатам отборочного тура</a>. 
ННГУ им Лобачевского для проведения олимпиады в 2017 году предоставляет, помимо классов со стационарными компьютерами,
еще один класс с ноутбуками, поэтому школьники, приглашенные с отборочного тура, разделены на две группы: школьники с более высокими результатами
будут писать тур за стационарными компьютерами (равно как и школьники, приглашенные напрямую), остальные будут писать тур за ноутбуками
(альтернативой было не приглашать этих участников вообще).</p>


<p>К участию в олимпиаде за "полноценными" компьютерами приглашаются:
<ul>
<li>все 9-классники и младше, набравшие на отборочном туре 183 баллов и больше,</li>
<li>все 10-классники, набравшие на отборочном туре 200 баллов и больше,</li>
<li>все 11-классники, набравшие на отборочном туре 218 баллов и больше.</li>
</ul>

<p>К участию в олимпиаде за ноутбуками приглашаются:
<ul>
<li>все 9-классники и младше, набравшие на отборочном туре от 175 до 182 баллов включительно,</li>
<li>все 10-классники, набравшие на отборочном туре от 189 до 199 баллов включительно,</li>
<li>все 11-классники, набравшие на отборочном туре 200 баллов, и при этом набравшие на районной олимпиаде 150 и более баллов.</li>
</ul>

<p>Кроме того, жюри решило пригласить к участию следующих школьников из Нижегородской области по итогам их выступления на областной олимпиаде:</p>
<ul>
<li>Маслов Владимир, 11 класс, школа 14 г. Балахна,</li>
<li>Тихонов Виталий, 11 класс, лицей г. Дзержинск,</li>
